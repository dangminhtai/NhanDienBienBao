import cv2
import numpy as np
import matplotlib.pyplot as plt
import os
from random import choice

def visualize_segmentation_rgb_normalized(image_path):
    img_bgr = cv2.imread(image_path)
    if img_bgr is None:
        print(f"Không tìm thấy ảnh tại {image_path}!")
        return

    img_rgb = cv2.cvtColor(img_bgr, cv2.COLOR_BGR2RGB)
    image_height, image_width, _ = img_rgb.shape

    # =======================================================
    # CHIẾN THUẬT 4: TIỀN XỬ LÝ ÁNH SÁNG BẰNG CLAHE
    # =======================================================
    lab = cv2.cvtColor(img_rgb, cv2.COLOR_RGB2LAB)
    l, a, b = cv2.split(lab)
    clahe = cv2.createCLAHE(clipLimit=2.5, tileGridSize=(8, 8))
    l_eq = clahe.apply(l)
    img_enhanced = cv2.cvtColor(cv2.merge((l_eq, a, b)), cv2.COLOR_LAB2RGB)

    # =======================================================
    # CHIẾN THUẬT 5: TOÁN HỌC NORMALIZED RGB R/(R+G+B)
    # =======================================================
    R = img_enhanced[:,:,0].astype(np.float32)
    G = img_enhanced[:,:,1].astype(np.float32)
    B = img_enhanced[:,:,2].astype(np.float32)
    Sum = R + G + B + 1e-6

    norm_R = R / Sum
    norm_B = B / Sum

    red_mask = np.uint8((norm_R > 0.40) & (R > 30)) * 255
    blue_mask = np.uint8((norm_B > 0.40) & (B > 30)) * 255

    # Raw Binary Mask (Ảnh b)
    raw_binary_mask = cv2.bitwise_or(red_mask, blue_mask)

    # Dùng hình thái học Morphological (Close/Open) để nối liền các mảng màu bị rách
    kernel = cv2.getStructuringElement(cv2.MORPH_ELLIPSE, (5, 5))
    filtered_mask = cv2.morphologyEx(raw_binary_mask, cv2.MORPH_CLOSE, kernel)
    filtered_mask = cv2.morphologyEx(filtered_mask, cv2.MORPH_OPEN, kernel)

    # =======================================================
    # CHIẾN THUẬT 2: LỌC RÁC THEO HÌNH HỌC (SHAPE-BASED)
    # =======================================================
    contours, _ = cv2.findContours(filtered_mask, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)

    final_clean_mask = np.zeros_like(filtered_mask) # Để vẽ các contour đã lọc
    num_boxes = 0
    for c in contours:
        x, y, w, h = cv2.boundingRect(c)
        area = w * h
        aspect_ratio = float(w) / h

        if 200 < area < 16000 and 0.4 < aspect_ratio < 1.9:
            cv2.drawContours(final_clean_mask, [c], -1, 255, thickness=-1) # Vẽ contour lên mask cuối cùng
            num_boxes += 1

    # Vẽ biểu đồ
    plt.figure(figsize=(18, 6))

    plt.subplot(1, 3, 1)
    plt.imshow(img_rgb)
    plt.title('(a) Original Image', fontsize=14)
    plt.axis('off')

    plt.subplot(1, 3, 2)
    plt.imshow(raw_binary_mask, cmap='gray')
    plt.title('(b) Raw Binary Mask (RGB Normalized)', fontsize=14)
    plt.axis('off')

    plt.subplot(1, 3, 3)
    plt.imshow(final_clean_mask, cmap='gray')
    plt.title(f'(c) Filtered Mask ({num_boxes} boxes)', fontsize=14)
    plt.axis('off')

    plt.tight_layout()
    plt.show()

    print(f"Đã trực quan hóa phân đoạn RGB Normalized cho ảnh: {os.path.basename(image_path)}")


visualize_segmentation_rgb_normalized(TEST_DIR +  '00101.ppm')