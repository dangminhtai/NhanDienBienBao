def propose_bounding_boxes(image_path):
    img_bgr = cv2.imread(image_path)
    if img_bgr is None:
        return [], None

    img_rgb = cv2.cvtColor(img_bgr, cv2.COLOR_BGR2RGB)
    image_height, image_width, _ = img_rgb.shape

    # =======================================================
    # CHIẾN THUẬT 4: TIỀN XỬ LÝ ÁNH SÁNG BẰNG CLAHE
    # =======================================================
    # Chuyển sang không gian LAB để tách riêng kênh Sáng (L) và kênh Màu (A, B)
    lab = cv2.cvtColor(img_rgb, cv2.COLOR_RGB2LAB)
    l, a, b = cv2.split(lab)

    # Áp dụng CLAHE để kéo sáng các vùng bóng râm (như mép đường có cây)
    clahe = cv2.createCLAHE(clipLimit=2.5, tileGridSize=(8, 8))
    l_eq = clahe.apply(l)

    # Gộp lại và chuyển về RGB
    img_enhanced = cv2.cvtColor(cv2.merge((l_eq, a, b)), cv2.COLOR_LAB2RGB)

    # =======================================================
    # CHIẾN THUẬT 5: TOÁN HỌC NORMALIZED RGB R/(R+G+B)
    # =======================================================
    # Chuyển kiểu float để tính toán không bị tràn số lượng
    R = img_enhanced[:,:,0].astype(np.float32)
    G = img_enhanced[:,:,1].astype(np.float32)
    B = img_enhanced[:,:,2].astype(np.float32)

    # Cộng thêm 1e-6 để tránh lỗi chia cho 0 (ZeroDivisionError)
    Sum = R + G + B + 1e-6

    norm_R = R / Sum
    norm_B = B / Sum

    # Tạo Mask (Mặt nạ):
    # Biển cấm (Đỏ): Tỉ lệ đỏ > 40% VÀ cường độ pixel đỏ không quá đen (>30)
    # Biển hiệu lệnh (Xanh): Tỉ lệ xanh > 40% VÀ cường độ pixel xanh không quá đen (>30)
    red_mask = np.uint8((norm_R > 0.40) & (R > 30)) * 255
    blue_mask = np.uint8((norm_B > 0.40) & (B > 30)) * 255

    final_mask = cv2.bitwise_or(red_mask, blue_mask)

    # Dùng hình thái học Morphological (Close/Open) để nối liền các mảng màu bị rách
    kernel = cv2.getStructuringElement(cv2.MORPH_ELLIPSE, (5, 5))
    final_mask = cv2.morphologyEx(final_mask, cv2.MORPH_CLOSE, kernel)
    final_mask = cv2.morphologyEx(final_mask, cv2.MORPH_OPEN, kernel)

    # =======================================================
    # CHIẾN THUẬT 2: LỌC RÁC THEO HÌNH HỌC (SHAPE-BASED)
    # =======================================================
    contours, _ = cv2.findContours(final_mask, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)

    boxes = []
    for c in contours:
        x, y, w, h = cv2.boundingRect(c)
        area = w * h
        aspect_ratio = float(w) / h

        # Điều kiện khắt khe:
        # 1. Diện tích phải đủ lớn (> 200 pixel) để nhìn rõ
        # 2. Tỷ lệ khung hình từ 0.6 đến 1.4 (Loại bỏ các vệt màu dài ngoằng hoặc dẹt)
        if 200 < area < 16000 and 0.4 < aspect_ratio < 1.9:
            boxes.append((x, y, w, h))

    return boxes, img_rgb

def propose_bounding_boxes_hybrid(image_path):
    img_bgr = cv2.imread(image_path)
    if img_bgr is None:
        return [], None

    img_rgb = cv2.cvtColor(img_bgr, cv2.COLOR_BGR2RGB)
    image_height, image_width, _ = img_rgb.shape

    # 1. TIỀN XỬ LÝ (CLAHE và Grayscale cho MSER)
    gray = cv2.cvtColor(img_rgb, cv2.COLOR_RGB2GRAY)
    clahe = cv2.createCLAHE(clipLimit=2.0, tileGridSize=(8, 8))
    gray_eq = clahe.apply(gray)

    # 2. CHUẨN BỊ BẢN ĐỒ MÀU NORMALIZED RGB (Để kiểm duyệt)
    img_float = img_rgb.astype(np.float32)
    R = img_float[:,:,0]
    G = img_float[:,:,1]
    B = img_float[:,:,2]
    Sum = R + G + B + 1e-6

    norm_R = R / Sum
    norm_B = B / Sum

    # 3. CHẠY MSER TÌM HÌNH KHỐI
    mser = cv2.MSER_create(delta=5, min_area=150, max_area=15000)
    regions, boundingBoxes = mser.detectRegions(gray_eq)

    # Lọc box trùng lặp nhanh
    boundingBoxes = cv2.groupRectangles(boundingBoxes.tolist(), groupThreshold=1, eps=0.2)[0]

    final_boxes = []

    for box in boundingBoxes:
        x, y, w, h = box
        aspect_ratio = float(w) / h

        # A. Lọc hình học (Chỉ lấy hình tương đối vuông/tròn)
        if 0.7 <= aspect_ratio <= 1.3:

            # B. Lọc màu sắc (Kiểm duyệt chéo)
            # Lấy vùng trung tâm của box để check màu (tránh lấy viền)
            cx, cy = x + w//2, y + h//2
            check_w, check_h = int(w * 0.5), int(h * 0.5)
            rx1, ry1 = max(0, cx - check_w//2), max(0, cy - check_h//2)
            rx2, ry2 = min(image_width, rx1 + check_w), min(image_height, ry1 + check_h)

            roi_norm_R = norm_R[ry1:ry2, rx1:rx2]
            roi_norm_B = norm_B[ry1:ry2, rx1:rx2]

            # Tính trung bình tỷ lệ Đỏ và Xanh trong vùng này
            mean_red = np.mean(roi_norm_R) if roi_norm_R.size > 0 else 0
            mean_blue = np.mean(roi_norm_B) if roi_norm_B.size > 0 else 0

            # Nếu tỷ lệ Đỏ > 35% HOẶC Xanh > 35%, ta mới chấp nhận nó là biển báo
            if mean_red > 0.1 or mean_blue > 0.1:

                # C. [HACK PADDING] Bơm thêm 30% diện tích và ép hình vuông chuẩn
                box_size = int(max(w, h) * 1.3)

                new_x1 = max(0, cx - box_size // 2)
                new_y1 = max(0, cy - box_size // 2)
                new_x2 = min(image_width, new_x1 + box_size)
                new_y2 = min(image_height, new_y1 + box_size)

                real_w = new_x2 - new_x1
                real_h = new_y2 - new_y1

                final_boxes.append((new_x1, new_y1, real_w, real_h))

    return final_boxes, img_rgb

def bounding_boxes_3(image_path):
    img_bgr = cv2.imread(image_path)
    if img_bgr is None:
        return [], None

    img_rgb = cv2.cvtColor(img_bgr, cv2.COLOR_BGR2RGB)

    # 1. Chuyển sang HSV và Cân bằng sáng (CLAHE) trên kênh Value (V)
    hsv = cv2.cvtColor(img_rgb, cv2.COLOR_RGB2HSV)
    h, s, v = cv2.split(hsv)
    clahe = cv2.createCLAHE(clipLimit=2.0, tileGridSize=(8, 8))
    v_eq = clahe.apply(v)
    hsv_eq = cv2.merge((h, s, v_eq))

    # 2. Lọc màu Đỏ và Xanh (Ngưỡng S > 70, V > 50)
    lower_red1 = np.array([0, 70, 50])
    upper_red1 = np.array([10, 255, 255])
    lower_red2 = np.array([160, 70, 50])
    upper_red2 = np.array([180, 255, 255])
    lower_blue = np.array([100, 70, 50])
    upper_blue = np.array([130, 255, 255])

    mask_red1 = cv2.inRange(hsv_eq, lower_red1, upper_red1)
    mask_red2 = cv2.inRange(hsv_eq, lower_red2, upper_red2)
    mask_blue = cv2.inRange(hsv_eq, lower_blue, upper_blue)

    raw_binary_mask = mask_red1 | mask_red2 | mask_blue

    # 3. Hình thái học (Dọn rác hạt tiêu)
    kernel = cv2.getStructuringElement(cv2.MORPH_ELLIPSE, (5, 5))
    filtered_mask = cv2.morphologyEx(raw_binary_mask, cv2.MORPH_CLOSE, kernel)
    filtered_mask = cv2.morphologyEx(filtered_mask, cv2.MORPH_OPEN, kernel)

    # 4. Tìm Contours và Lọc Hình học
    contours, _ = cv2.findContours(filtered_mask, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)

    boxes = []
    for c in contours:
        x, y, w, h = cv2.boundingRect(c)
        area = w * h
        aspect_ratio = float(w) / h

        # Áp dụng thông số chuẩn
        if 200 < area < 16000 and 0.4 < aspect_ratio < 1.9:
            boxes.append((x, y, w, h))

    return boxes, img_rgb

