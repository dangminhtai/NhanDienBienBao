def recognize_traffic_signs_in_scene(image_path, iou_threshold_nms=0.3):
    print(f"Bắt đầu xử lý ảnh: {image_path}")

    # a. Gọi hàm đề xuất vùng
    proposed_boxes_xywh, original_image_rgb = propose_bounding_boxes_hybrid(image_path)

    if original_image_rgb is None:
        print(f"Lỗi: Không thể tải hoặc xử lý ảnh từ {image_path}")
        return

    result_image = original_image_rgb.copy()
    image_height, image_width, _ = original_image_rgb.shape

    detected_boxes = []
    detection_scores = []

    print(f"  Phát hiện: Đang kiểm tra {len(proposed_boxes_xywh)} hộp đề xuất...")
    # b. Vòng lặp HOG + Binary SVM (Có Hack Padding)
    for (x, y, w, h) in proposed_boxes_xywh:

        # --- [HACK TẠO KHOẢNG ĐỆM VÀ ÉP VUÔNG] ---
        center_x = x + w // 2
        center_y = y + h // 2

        # Lấy cạnh lớn nhất, cộng thêm 30% padding để lấy được bối cảnh xung quanh
        box_size = int(max(w, h) * 1.3)

        # Tính toán tọa độ mới
        x1 = max(0, center_x - box_size // 2)
        y1 = max(0, center_y - box_size // 2)
        x2 = min(image_width, x1 + box_size)
        y2 = min(image_height, y1 + box_size)

        # Tính lại w, h thực tế (phòng trường hợp hộp bị cắt ở mép ảnh)
        real_w = x2 - x1
        real_h = y2 - y1
        # -----------------------------------------

        cropped_roi = original_image_rgb[y1:y2, x1:x2]

        if cropped_roi.shape[0] > 0 and cropped_roi.shape[1] > 0:
            resized_roi = cv2.resize(cropped_roi, (32, 32))
            gray_roi = rgb2gray(resized_roi)

            # Trích xuất đặc trưng HOG
            hog_features = hog(gray_roi, orientations=9, pixels_per_cell=(8, 8),
                               cells_per_block=(2, 2), feature_vector=True, block_norm='L2-Hys')

            # Scale bằng biến detect_scaler của mô hình nhị phân (324 chiều)
            scaled_hog_features = detect_scaler.transform(hog_features.reshape(1, -1))

            # BỎ HÀM PREDICT MẶC ĐỊNH. Tính trực tiếp điểm khoảng cách Margin:
            decision_score = best_svm_model.decision_function(scaled_hog_features)[0]

            # Hạ ngưỡng kiểm duyệt xuống -0.2 (Biển báo hơi mờ/méo vẫn qua được)
            if decision_score > -0.2:
                # Phải lưu lại [x1, y1, real_w, real_h] của hộp MỚI (đã có padding)
                detected_boxes.append([x1, y1, real_w, real_h])
                # Shift scores to be positive for NMSBoxes, while preserving relative order
                detection_scores.append(decision_score + 0.21) # Add a small constant to make scores > 0

    print(f"  Phát hiện: Đã tìm thấy {len(detected_boxes)} hộp dương tính trước NMS.")

    detected_boxes_np = np.array(detected_boxes, dtype=np.int32)
    detection_scores_np = np.array(detection_scores, dtype=np.float32)

    final_indices = []
    if len(detected_boxes_np) > 0:
        # --- [SỬA LỖI TÀNG HÌNH CỦA OPENCV] ---
        # score_threshold phải >= 0. Chúng ta đã shift detection_scores_np lên trên 0.
        indices = cv2.dnn.NMSBoxes(detected_boxes_np.tolist(), detection_scores_np.tolist(),
                                   score_threshold=0.0, nms_threshold=iou_threshold_nms) # Changed to 0.0
        if len(indices) > 0:
            final_indices = indices.flatten()

    final_boxes = []
    final_labels = []
    final_detection_scores = []

    print(f"  NMS: {len(final_indices)} hộp vượt qua NMS.")

    # e. Nhận diện (CNN + SVM Đa lớp)
    for i in final_indices:
        x, y, w, h = detected_boxes_np[i]
        # The original decision_score (before shifting) might be useful if needed later
        # For display, we can use the shifted score or recalculate if needed
        score_detection = detection_scores_np[i]

        x1, y1 = max(0, x), max(0, y)
        x2, y2 = min(image_width, x + w), min(image_height, y + h)

        cropped_roi_recognition = original_image_rgb[y1:y2, x1:x2]

        if cropped_roi_recognition.shape[0] > 0 and cropped_roi_recognition.shape[1] > 0:
            resized_roi_recognition = cv2.resize(cropped_roi_recognition, (32, 32))
            normalized_roi = resized_roi_recognition.astype("float32") / 255.0
            expanded_roi = np.expand_dims(normalized_roi, axis=0)

            cnn_features = cnn_feature_extractor.predict(expanded_roi, verbose=0)
            scaled_cnn_features = svm_scaler.transform(cnn_features)

            class_id = final_hybrid_svm.predict(scaled_cnn_features)[0]
            class_name = CLASS_NAMES.get(class_id, f"Unknown Sign ({class_id})")

            final_boxes.append((x, y, w, h))
            final_labels.append(class_name)
            # When storing final_detection_scores, you might want to store the original decision_score
            # or clarify that this is the shifted score used for NMS.
            # For now, let's keep the shifted score to reflect the NMS confidence.
            final_detection_scores.append(score_detection)

    print(f"  Nhận diện: Đã nhận diện {len(final_boxes)} biển báo cuối cùng.")

    for (x, y, w, h), label in zip(final_boxes, final_labels):
        color = (0, 255, 0)
        thickness = 3
        cv2.rectangle(result_image, (x, y), (x + w, y + h), color, thickness)
        cv2.putText(result_image, label, (x, y - 10), cv2.FONT_HERSHEY_SIMPLEX, 0.7, color, 2)

    plt.figure(figsize=(15, 10))
    plt.imshow(result_image)
    plt.title(f'Phát hiện và Nhận diện biển báo cho ảnh: {os.path.basename(image_path)}')
    plt.axis('off')
    plt.show()

    return final_boxes, final_labels, final_detection_scores

